
public class Person {
	
	private String vorname;
	private String nachname;
	private int alter;


public  Person(){
}
	public String getvorname()
	{
		return vorname;
	}
	
	public String getnachname()
	{
		return nachname;
	}
	
	public int getalter()
	{
		return alter;
	}
	
	public void setvorname(String vn)
	{
		vorname = vn;
	}
	
	public void setnachname(String nn)
	{
		nachname = nn;
	}
	
	public void setalter(int alt)
	{
		alter = alt;
	}
		
	
	
}

